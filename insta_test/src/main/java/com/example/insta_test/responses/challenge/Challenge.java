package com.example.insta_test.responses.challenge;



public class Challenge {
    private String url;
    private String api_path;
    private String challenge_context;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getApi_path() {
        return api_path;
    }

    public void setApi_path(String api_path) {
        this.api_path = api_path;
    }

    public String getChallenge_context() {
        return challenge_context;
    }

    public void setChallenge_context(String challenge_context) {
        this.challenge_context = challenge_context;
    }
}
